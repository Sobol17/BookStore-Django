from django.utils.text import slugify

_TRANSLIT_MAP = {
    'а': 'a',
    'б': 'b',
    'в': 'v',
    'г': 'g',
    'д': 'd',
    'е': 'e',
    'ё': 'yo',
    'ж': 'zh',
    'з': 'z',
    'и': 'i',
    'й': 'y',
    'к': 'k',
    'л': 'l',
    'м': 'm',
    'н': 'n',
    'о': 'o',
    'п': 'p',
    'р': 'r',
    'с': 's',
    'т': 't',
    'у': 'u',
    'ф': 'f',
    'х': 'kh',
    'ц': 'ts',
    'ч': 'ch',
    'ш': 'sh',
    'щ': 'shch',
    'ъ': '',
    'ы': 'y',
    'ь': '',
    'э': 'e',
    'ю': 'yu',
    'я': 'ya',
}

for key, value in list(_TRANSLIT_MAP.items()):
    _TRANSLIT_MAP[key.upper()] = value


def translit_text(value):
    if value is None:
        return ''
    text = str(value)
    return ''.join(_TRANSLIT_MAP.get(ch, ch) for ch in text)


def slugify_translit(value):
    return slugify(translit_text(value))
